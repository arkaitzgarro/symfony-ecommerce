namespace QBH\StoreProductBundle\CompilerPass;

use Mmoreram\SimpleDoctrineMapping\CompilerPass\Abstracts\AbstractMappingCompilerPass;
use Symfony\Component\DependencyInjection\ContainerBuilder;

/**
 * Class MappingCompilerPass
 * @package QBH\StoreProductBundle\CompilerPass
 */
class MappingCompilerPass extends AbstractMappingCompilerPass
{
    /**
     * You can modify the container here before it is dumped to PHP code.
     *
     * @param ContainerBuilder $container
     *
     * @api
     */
    public function process(ContainerBuilder $container)
    {
        $this
            ->addEntityMapping(
                $container,
                'qbh.store.product.entity.product_tag.manager',
                'qbh.store.product.entity.product_tag.class',
                'qbh.store.product.entity.product_tag.mapping_file',
                'qbh.store.product.entity.product_tag.enabled'
            );
    }
}